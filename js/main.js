function createTotalSalesChart(value){
    var ctx = document.getElementById("totalSales").getContext("2d");

    const colors = {
          lightBlue: {
            fill: '#6fccdd',
          },
    };

    const yearly = [
        this.data.filter((item)=>{
            return new Date(item.Order_Date) >= new Date("01-01-2015");
        }).length, 
        this.data.filter((item)=>{
            return new Date(item.Order_Date) >= new Date("01-01-2016");
        }).length,
        this.data.filter((item)=>{
            return new Date(item.Order_Date) >= new Date("01-01-2017");
        }).length, 
        this.data.filter((item)=>{
            return new Date(item.Order_Date) >= new Date("01-01-2018");
        }).length,
        0
    ];

    const monthly = [
        this.data.filter((item)=>{
            return new Date(item.Order_Date) >= new Date("01-01-2018") && new Date(item.Order_Date) <= new Date("06-01-2018");
        }).length,
        this.data.filter((item)=>{
            return new Date(item.Order_Date) >= new Date("07-01-2018") && new Date(item.Order_Date) <= new Date("11-01-2018");
        }).length,
        this.data.filter((item)=>{
            return new Date(item.Order_Date) >= new Date("12-01-2018") && new Date(item.Order_Date) <= new Date("16-01-2018");
        }).length,
        this.data.filter((item)=>{
            return new Date(item.Order_Date) >= new Date("17-01-2018") && new Date(item.Order_Date) <= new Date("21-01-2018");
        }).length,
        this.data.filter((item)=>{
            return new Date(item.Order_Date) >= new Date("22-01-2018") && new Date(item.Order_Date) <= new Date("26-01-2018");
        }).length,
        this.data.filter((item)=>{
            return new Date(item.Order_Date) >= new Date("27-01-2018") && new Date(item.Order_Date) <= new Date("31-01-2018");
        }).length,
    ];

    const myChart = new Chart(ctx, {
        type: 'line',
        labels: ["Sales"],
        data: {
            labels: value === 'yearly' ? ["2015", "2016", "2017", "2018", "2019"]:['1-18', '6-18' , '11-18', '16-18' , '21-18', '26-18' ,'31-18'],
            datasets: [{
                data: value==='yearly'?yearly:monthly,
                fill: true,
                backgroundColor: colors.lightBlue.fill,
                borderWidth: 1 
            }
            ]
        },
        options: {
            responsive: true,
            maintainAspectRatio: false,
        }
    });
    
}

